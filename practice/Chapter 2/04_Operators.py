# Arithmetic operators (+, -, *, / etc.) is a arithmetic operator

a = 32
b = 4
c = a+b    #
print(c)

# Assignment operators (=, +=, -= etc.)

a = 4-2     #
b = 6
b += 3      #increment in b
print(a+b) 

# Comparison operators (===+, >, >=, <, != etc.)

d = 5>2
print(d)

# Logical operators (and, or, not)

e = True or False
f = True and False
g = not True
print(e)
print(f)
print(g)
